package com.example.scalpingBot.enums;

/**
 * Перечисление, определяющее уровень риска для торговых стратегий.
 * Уровень риска может влиять на такие параметры, как размер позиции,
 * процент стоп-лосса и тейк-профита.
 */
public enum RiskLevel {
    /**
     * Низкий уровень риска (консервативная стратегия).
     * Подразумевает меньший размер позиций и более жесткие стоп-лоссы.
     */
    LOW,

    /**
     * Средний, сбалансированный уровень риска.
     * Стандартные параметры для большинства рыночных условий.
     */
    MEDIUM,

    /**
     * Высокий уровень риска (агрессивная стратегия).
     * Подразумевает увеличенный размер позиций и более широкие стоп-лоссы
     * для потенциально большей прибыли (и больших убытков).
     */
    HIGH
}
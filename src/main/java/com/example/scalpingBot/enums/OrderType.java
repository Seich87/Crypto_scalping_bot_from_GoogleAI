package com.example.scalpingBot.enums;

/**
 * Перечисление типов ордеров, используемых на бирже.
 */
public enum OrderType {
    /**
     * Рыночный ордер, исполняется немедленно по текущей рыночной цене.
     */
    MARKET,

    /**
     * Лимитный ордер, исполняется по указанной или лучшей цене.
     */
    LIMIT,

    /**
     * Стоп-лосс ордер (рыночный). Триггер для продажи по рыночной цене при достижении стоп-цены.
     */
    STOP_LOSS,

    /**
     * Тейк-профит ордер (рыночный). Триггер для продажи по рыночной цене при достижении профитной цены.
     */
    TAKE_PROFIT,

    /**
     * Стоп-лосс лимитный ордер. При достижении стоп-цены выставляется лимитный ордер по указанной лимитной цене.
     */
    STOP_LOSS_LIMIT,

    /**
     * Тейк-профит лимитный ордер. При достижении профитной цены выставляется лимитный ордер по указанной лимитной цене.
     */
    TAKE_PROFIT_LIMIT;
}
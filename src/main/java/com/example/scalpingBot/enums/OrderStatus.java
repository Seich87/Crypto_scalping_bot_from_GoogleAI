package com.example.scalpingBot.enums;

/**
 * Перечисление, представляющее возможные статусы ордера на бирже.
 */
public enum OrderStatus {
    /**
     * Ордер был отправлен на биржу, но еще не исполнен.
     * Это начальный статус для лимитных ордеров.
     */
    NEW,

    /**
     * Ордер был полностью исполнен.
     */
    FILLED,

    /**
     * Ордер был частично исполнен.
     * Часть объема ордера была куплена/продана.
     */
    PARTIALLY_FILLED,

    /**
     * Ордер был отменен пользователем или системой.
     */
    CANCELED,

    /**
     * Ордер находится в процессе отмены.
     */
    PENDING_CANCEL,

    /**
     * Ордер был отклонен биржей (например, из-за недостатка средств).
     */
    REJECTED,

    /**
     * Срок действия ордера истек.
     */
    EXPIRED
}